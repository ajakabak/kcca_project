        <p data-para-word-count="27">Kampala &mdash; Presidential aspirants could face fines for defying city authorities' orders not to litter the city with campaign posters, Kampala Capital City Authority (KCCA) has warned.</p>
        <p data-para-word-count="16">Mr Robert Kalumba, the KCCA deputy spokesperson, yesterday said whoever erected campaigning posters did so illegally.</p>
        <p data-para-word-count="59">&quot;Any good leader sets precedent for others to follow. We have already communicated to all political parties and they know what will happen for defying the law,&quot; Mr Kalumba said in an interview. According to the Kampala City Council Maintenance of Law and Order Ordinance 2006, no poster shall be erected in the city without the approval of KCCA.</p>
        <p data-para-word-count="24">&quot;We have also communicated to the Independent Electoral Commission on the matter and each political party leader will face the law,&quot; Mr Kalumba said.</p>
